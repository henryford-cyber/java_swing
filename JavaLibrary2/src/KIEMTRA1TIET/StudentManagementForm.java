    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        StringBuilder sb = new StringBuilder();
        if (txtMasv.getText().equals("")) {
            sb.append("Mã Sinh viên không được để trống!");
            txtMasv.setBackground(Color.red);

        } else {
            txtMasv.setBackground(Color.white);
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb);
            return;

        }
        try {
            Student student = new Student();
            student.setMaSV(Integer.parseInt(txtMasv.getText()));
            student.setHoTen(txtHoten.getText());
            student.setEmail(txtEmail.getText());
            student.setSoDT(txtSdt.getText());
            student.setGioiTinh(txtGioitinh.getText());
            student.setDiaChi(txtDiachi.getText());

            StudentDAO dao = new StudentDAO();
            dao.insert(stud);
            JOptionPane.showMessageDialog(this, "Sinh viên mới đã được lưu vào CSDL của bạn");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi rồi đại vương ơi !" + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnTimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimkiemActionPerformed
        if (txtMasv.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Cần phải nhập mã sinh viên để tìm kiếm");
            return;
        }
        try {
            StudentDAO dao = new StudentDAO();
            Student student = dao.FindByid(Integer.parseInt(txtMasv.getText()));
            if (student != null) {
                txtHoten.setText(student.getHoTen());
                txtEmail.setText(student.getEmail());
                txtSdt.setText(student.getSoDT());
                txtGioitinh.setText(student.getEmail());
                txtDiachi.setText(student.getDiaChi());

            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy Sinh viên !");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi " + e.getMessage());
            e.printStackTrace();

        }
    }//GEN-LAST:event_btnTimkiemActionPerformed

