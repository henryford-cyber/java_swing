    public void capnhap() {
        if (txtMataikhoan.equals("")) {
            JOptionPane.showMessageDialog(this, "Cần nhập mã tài khoản để cập nhập!");
        }
        if (txtTendangnhap.equals("")) {
            JOptionPane.showMessageDialog(this, "Cần nhập tên đăng nhập để cập nhập");
        }
        if (txtMatkhau.equals("")) {
            JOptionPane.showMessageDialog(this, "Cần nhập Mật khẩu  để cập nhập");
        }
        try {
            String selectdate = ((JTextField) txtNgaysinh.getDateEditor().getUiComponent()).getText();
            AccountDao daoo = new AccountDao();
            String id = daoo.FindByID(txtMataikhoan.getText()).getMataikhoan();
            Account account = new Account(
                    id,
                    txtHovaten.getText(),
                    selectdate,
                    txtCmnd.getText(),
                    txtTendangnhap.getText(),
                    txtMatkhau.getText(),
                    Integer.parseInt(txtSdt.getText()),
                    txtDiachi.getText(),
                    cbbGioitinh.getSelectedItem().toString(),
                    cbbTinhtrang.getSelectedItem().toString(),
                    cbbVaitro.getSelectedItem().toString()
            );
            daoo.update(account);
            loadAcccount();
            JOptionPane.showMessageDialog(this, "Đã cập nhập thành công!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi rồi đại vương ơi !" + e.getMessage());
        }

    }
