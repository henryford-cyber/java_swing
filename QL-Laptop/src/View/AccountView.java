    private void btnTimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimkiemActionPerformed
        if (tblTimkiem.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Cần phải nhập tên tài khoản để tìm kiếm");
            return;
        }
        try {
            AccountDao dao = new AccountDao();
            Account account = dao.FindByID(tblTimkiem.getText());
            if (account != null) {
                txtMataikhoan.setText(account.getMataikhoan());
                txtHovaten.setText(account.getHoten());
                //txtNgaysinh.setText(account.getMatkhau());
                txtCmnd.setText(account.getCmnd());
                txtDiachi.setText(account.getDiachi());
                txtTendangnhap.setText(account.getMatkhau());
                txtMatkhau.setText(account.getMatkhau());
                //txtSdt.setText(Integer.parseInt(account.getDienthoai()));

            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy tài khoản !");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi " + e.getMessage());

        }
    }//GEN-LAST:event_btnTimkiemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        StringBuilder sb = new StringBuilder();

        if (txtMataikhoan.getText().equals("")) {
            sb.append("Nhập tên sinh viên muốn xóa!");
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb);
            return;
        }
        if (JOptionPane.showConfirmDialog(this, "Bạn có chắc là muốn xóa không!") == JOptionPane.YES_OPTION) {

            try {
                AccountDao dao = new AccountDao();
                dao.Delete(txtMataikhoan.getText());
                loadAcccount();
                JOptionPane.showMessageDialog(this, "Đã xóa sinh viên !");
                Lam_sach();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi rồi đại vương ơi !" + e.getMessage());
            }
        } else {
            return;
        }
    }//GEN-LAST:event_btnXoaActionPerformed


