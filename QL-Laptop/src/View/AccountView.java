    public void DangKytaikhoan() {
        String ID = new String(txtMataikhoan.getText());
        if (ID.equals("")) {
            chuanhapID.setText("Bạn chưa nhập Mã tài khoản !");
            txtMataikhoan.requestFocus();
            return;
        }
        AccountDao nhap = new AccountDao();
        if (nhap.checkNum(this.txtMataikhoan.getText()) == false) {
            JOptionPane.showMessageDialog(null, "Ma tai khoan bạn phải nhập số");
        }
        String tendangnhap = new String(txtTendangnhap.getText());
        if (tendangnhap.equals("")) {
            JOptionPane.showMessageDialog(null, "Bạn chưa điền tên đăng nhập");
        }

        if (nhap.checkNum(this.txtCmnd.getText()) == false) {
            JOptionPane.showMessageDialog(null, "Chứng minh thư bạn phải nhập số");
        }
//        if (txtCmnd.getText().length() >= 9) {
//            chuanhapCMND.setText("Độ dài CMND pải lớp hơn hoặc bằng 9 Số !!");
//        }
        String pw = new String(txtMatkhau.getText());
        if (pw.equals("")) {
            chuanhapMK.setText("Bạn chưa nhập mật khẩu");
            txtMatkhau.requestFocus();
            return;
        }
        if (txtMatkhau.getText().length() < 8) {
            chuanhapMK.setText("Độ dài mật khẩu pải lớp hơn hoặc bằng 8 ký tự !!");
        }

        if (nhap.checkNum(this.txtSdt.getText()) == false) {
            JOptionPane.showMessageDialog(null, "Số điện thoại bạn phải nhập số");
        }
        String maTK = txtMataikhoan.getText();
        String tenDN = txtTendangnhap.getText();
        List<Account> listAcc = new ArrayList<>();
        listAcc = nhap.getAccount();
        for (Account taikhoan : listAcc) {
            if (taikhoan.getMataikhoan().equals(maTK)) {
                JOptionPane.showMessageDialog(null, "Mã tài khoản đã tồn tại !! vui lòng nhập lại ");

            }
            if (taikhoan.getTendangnhap().equals(tenDN)) {
                JOptionPane.showMessageDialog(null, "Tên đăng nhập đã tồn tại !! vui lòng nhập lại ");

            }
        }
        try {

            String selectdate = ((JTextField) txtNgaysinh.getDateEditor().getUiComponent()).getText();

            taikhoan.setMataikhoan(txtMataikhoan.getText());
            taikhoan.setHoten(txtHovaten.getText());
            taikhoan.setNgaysinh(selectdate);
            taikhoan.setCmnd(txtCmnd.getText());
            taikhoan.setTendangnhap(txtTendangnhap.getText());
            taikhoan.setMatkhau(txtMatkhau.getText());
            taikhoan.setDienthoai(Integer.parseInt(txtSdt.getText()));
            taikhoan.setDiachi(txtDiachi.getText());
            taikhoan.setGioitinh(cbbGioitinh.getSelectedItem().toString());
            taikhoan.setTinhtrang(cbbTinhtrang.getSelectedItem().toString());
            taikhoan.setVaitro(cbbVaitro.getSelectedItem().toString());

            AccountDao dao = new AccountDao();
            dao.insert(taikhoan);
            loadAcccount();
            JOptionPane.showMessageDialog(this, "Tạo tài khoản thành công!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi rồi đại vương ơi !" + e.getMessage());
        }

    }

