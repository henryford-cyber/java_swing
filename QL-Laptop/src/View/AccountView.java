    private void loadAcccount() {
        tblDanhsachtaikhoan.getTableHeader().setFont(new Font("Segoe UI", Font.BOLD, 16));
        tblDanhsachtaikhoan.getTableHeader().setOpaque(false);
        tblDanhsachtaikhoan.getTableHeader().setBackground(new Color(0, 153, 102));
        tblDanhsachtaikhoan.getTableHeader().setForeground(new Color(255, 255, 255));
        tblDanhsachtaikhoan.setRowHeight(25);
        dfModel = (DefaultTableModel) tblDanhsachtaikhoan.getModel();
        dfModel.setColumnIdentifiers(new Object[]{"Mã tài khoản ", "Họ và tên", "Ngày sinh", "Cmnd", "Tên đăng nhập", "SDT", "Địa chỉ", "Giới tính", "Vai trò"});
        try {
            String sql = "select * from taikhoan";
            Connection conn = null;
            PreparedStatement pstmt = null;

            conn = MySqlConnect.ConnectDb();
            pstmt = conn.prepareStatement(sql);

            ResultSet rs = pstmt.executeQuery();
            dfModel.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(7),
                    rs.getString(8),
                    rs.getString(9),
                    rs.getString(11)
                };
                dfModel.addRow(row);
            }
            dfModel.fireTableDataChanged();
            rs.close();
            pstmt.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());

        }
    }

