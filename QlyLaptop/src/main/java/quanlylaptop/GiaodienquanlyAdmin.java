    public void Updata_Table() {
        Vector cols = new Vector();
        cols.addElement("Mã Admin");
        cols.addElement("Tên Admin");
        cols.addElement("Năm Sinh");
        cols.addElement("Chứng Minh Thư");
        cols.addElement("Tên Đăng Nhập");
        cols.addElement("Mật Khẩu");
        cols.addElement("Điện Thoại");
        cols.addElement("Địa Chỉ");
        cols.addElement("Giới Tính");
//        
        Vector data = new Vector();

        String sql = "SELECT * FROM `admin`";
//        bang_data.setModel(new DefaultTableModel(data, cols));
        try {
            stmt = con.prepareStatement(sql);
            rs = stmt.executeQuery();
//           bang_data.setModel(DbUtils.resultSetToTableModel(rs));
            while (rs.next()) {
                Vector user = new Vector();
                user.addElement(rs.getString("maadmin"));
                user.addElement(rs.getString("tenadmin"));
                user.addElement(rs.getDate("ngaysinh"));
                user.addElement(rs.getString("cmnd"));
                user.addElement(rs.getString("tendangnhap"));
                user.addElement(rs.getString("matkhau"));
                user.addElement(rs.getString("dienthoai"));
                user.addElement(rs.getString("diachi"));
                user.addElement(rs.getString("gioitinh"));
                data.add(user);
            }
        } catch (Exception e) {
//           JOptionPane.showMessageDialog(null, cols);
        }
        ds_admin.setModel(new DefaultTableModel(data, cols));
    }

