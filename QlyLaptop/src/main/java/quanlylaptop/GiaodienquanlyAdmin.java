    public void Showdu_lieu_ra_bang() {
        int row = ds_admin.getSelectedRow();
        String id = ds_admin.getValueAt(row, 0).toString();
//
        if (row != -1) {
            txt_ID.setText(id);
            txt_tenadmin.setText(ds_admin.getValueAt(row, 1).toString());
            txt_ngaysinh.setDate((java.util.Date) (ds_admin.getValueAt(row, 2)));
            txt_cmt.setText(ds_admin.getValueAt(row, 3).toString());
            txt_tendangnhap.setText(ds_admin.getValueAt(row, 4).toString());
            txt_matkhau.setText(ds_admin.getValueAt(row, 5).toString());
            txt_dienthoai.setText(ds_admin.getValueAt(row, 6).toString());
            txt_diachi.setText(ds_admin.getValueAt(row, 7).toString());
            combox.setSelectedItem(ds_admin.getValueAt(row, 8).toString());
        }
    }

